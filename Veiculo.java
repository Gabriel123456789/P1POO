public class Veiculo {
    protected String placa;
    protected Integer anoVeiculo;

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public Integer getAno() {
        return anoVeiculo;
    }

    public void setAno(Integer ano) {
        this.anoVeiculo = ano;
    }
}
